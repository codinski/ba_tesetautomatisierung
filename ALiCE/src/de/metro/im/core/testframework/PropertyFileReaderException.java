package de.metro.im.core.testframework;

@SuppressWarnings("serial")
public class PropertyFileReaderException extends Exception {

	public PropertyFileReaderException() {
	}

	public PropertyFileReaderException(String arg0) {
		super(arg0);
	}

	public PropertyFileReaderException(Throwable arg0) {
		super(arg0);
	}

	public PropertyFileReaderException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public PropertyFileReaderException(String arg0, Throwable arg1,
			boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
